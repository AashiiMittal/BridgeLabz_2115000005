public class AverageMark {
    public static void main(String[] args) {
        int mathsMark = 94;
        int physicsMark = 95;
        int chemistryMark = 96;
        double average = (mathsMark + physicsMark + chemistryMark) / 3.0;
        System.out.println("Sam's average mark in PCM is " + average);
    }
}